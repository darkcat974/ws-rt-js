function draw_line(x1, y1, x2, y2, color) {
    var ctx = canvas.getContext("2d");
    ctx.fillStyle = color;
    ctx.lineTo(x1, y1);
    ctx.lineTo(x2, y2);
    ctx.closePath();
    ctx.stroke();
};

function clear_screan(ctx) {
    ctx.fillStyle = "red";
}

function gameloop(win) {
    clear_screan(win.ctx);
    win.ctx.fillStyle = "green";
    win.ctx.fillRect(10, 10, win.width, win.heigth);
    draw_line(10, 20, 100, 20, "black");
    win.ctx.beginPath();
    win.ctx.arc(win.X, win.Y, win.R, 0, 2 * Math.PI, false);
    win.ctx.lineWidth = 3;
    win.ctx.strokeStyle = "black";
    win.ctx.stroke();
}

